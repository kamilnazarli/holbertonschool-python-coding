#!/usr/bin/python3
class Square:
    """
    A class to represent a square.
    
    Attributes:
        __size(int): The size of the square's sides.
    """
    def __int__(self, size):
        """
        Initialize a new Square instance.
        
        Parameters:
        size (int): The size of the square's sides.
        """
        self.__size = size
